export const getAllTopics = (): IELTSPart[] => {
  return [...ieltsPart1Topics, ...ieltsPart2Topics, ...ieltsPart3Topics];
};

export const getTopicsByPart = (part: 1 | 2 | 3): IELTSPart[] => {
  switch (part) {
    case 1:
      return ieltsPart1Topics;
    case 2:
      return ieltsPart2Topics;
    case 3:
      return ieltsPart3Topics;
    default:
      return [];
  }
};

export const getRandomTopic = (part?: 1 | 2 | 3): IELTSPart => {
  const topics = part ? getTopicsByPart(part) : getAllTopics();
  const randomIndex = Math.floor(Math.random() * topics.length);
  return topics[randomIndex];
};